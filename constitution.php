					<h3 id="#preamble">Preamble</h3>
					<p>The name of this organization is the Northeastern Illinois University chapter of the Student Council for Exceptional Children.  Hereafter, this organization shall be referred to as the SCEC. </p>
					<p>The SCEC is dedicated to advancing excellence in education through quality professional preparation and service to exceptional learners.  The SCEC shall be an association of all future educators with an interest in special education.  In all endeavors, the SCEC promotes its members operating as a cohesive unit, with respect to the varied constituencies herein.  The SCEC will adhere to the highest standards of ethical conduct, professional accountability, and respect for diversity. </p>
					<p>The fundamental principles underlying this constitution are as follows:</p>
					<ol>
						<li>The SCEC supports equal educational opportunities for all learners and disseminates pertinent information to the public regarding the needs of exceptional children at the state, local and regional levels.</li>
						<li>  In addition, the SCEC desires to expand professional development in an effort to best serve the needs of exceptional learners and their families.  </li>
					</ol>
					<p>The remaining articles of this constitution shall detail the membership, organizational structure, and governance structure of the SCEC. The operating or governing bodies are identified along with membership, responsibilities and operating procedures. The structures and functions described herein are intended to support the National Council for Exceptional Children and its activities toward the attainment of its mission. </p>
					<a href="#home">Back to top</a>
					
					<h3 id="CI">Article I</h3>
					<h4>Membership Requirement</h4>
					<ol>
						<li>The membership of the organization shall consist of the following:</li>
						<li>Members of the organization shall be currently enrolled students who have attended at least 3 meetings of the organization.</li>
						<li>The minimum academic grade point average for general membership shall be 2.75.</li>
						<li> Members of the organization shall be current members of the National Council for Exceptional Children paying membership dues of $67.</li>
						<li> Membership in the organization will not be denied to any person(s) on the basis of age, color, disability, national origin, race, religion, sex, sexual orientation, marital status, or veteran status.</li>
					</ol>
					<a href="#home">Back to top</a>
					
					<h3 id="CII">Article II</h3>
					<h4>Officers</h4>
					<ol>
						<li> The Executive Committee shall consist of the following Officers: President, Vice President, Secretary, Treasurer and State Liaison.</li>
						<li> Students must be enrolled for at least 3 hours and have a 2.75 academic grade point average to be eligible to serve as an Executive Officer. The 3 credit hours and 2.75 GPA must exist at the time of election and maintained throughout the term of office.</li>
						<li>The SCEC will appoint (with approval by membership) a student liaison to serve as a State Representative for the National Council for Exceptional Children.</li>
					</ol>
					<a href="#home">Back to top</a>
					
					<h3 id="CIII">Article III</h3>
					<h4>Advisor</h4>
					<ol>
						<li>The advisor shall be recommended by the Executive Committee and selected by the regular membership of the organization.</li>
						<li>The advisor must agree to serve and submit a letter indicating willingness to serve to the Student Activities Office.</li>
					</ol>
					<a href="#home">Back to top</a>
					
					<h3 id="CIV">Article IV</h3>
					<h4>Powers and Functions of the Executive Committee</h4>
					<p>Section A. President</p>
					<ol>
						<li>Shall preside at the meetings of the organization.</li>
						<li> Shall act as ex-officio member of all committees.</li>
					</ol>
					<p>Section B. Vice President</p>
					<ol>
						<li> Shall file charter renewal forms each July (and whenever officers change) with the Student Activities Office.</li>
						<li>Shall preside at meetings of the organization in the absence of the president.</li>
					</ol>
						<p>Section C. Treasurer</p>
					<ol>
						<li>Shall collect all funds raised, make deposits to the University Cashier's Office and submit deposit receipts to the Student Activities Office.</li>
						<li> Shall report to the organization a financial report after fundraising efforts.</li>
					</ol>
					<p>Section D. Secretary</p>
					<ol>
						<li>Shall maintain organization meeting minutes and records.</li>
						<li> Shall transmit to the Student Activities Office minutes and signatures of those attending meetings whenever:
							<ol>
								<li>Officers are elected.</li>
								<li>Advisor is chosen.</li>
								<li>Decisions are made to request the expenditure of funds.</li>
								<li>Changes are made to constitution and/or by-laws.</li>
							</ol>
						</li>
						<li>Shall make sure current organization Constitution and By-Laws are on file in the Student Activities Office.</li>
					</ol>
					<p>Shall make sure current organization Constitution and By-Laws are on file in the Student Activities Office.</p>
					<p>Section F. Standing Committees</p>
					<p>Fundraising Committee</p>
					<p>Membership of standing committees shall be recommended by the Executive Committee and approved by the membership. The Executive Committee shall appoint the Committee Chair.</p>
					<a href="#home">Back to top</a>
				
					<h3 id="CV">Article V</h3>
					<h4>Meetings</h4>
					<p>The organization shall meet at least four times during each semester of the regular academic year.</p>
					<a href="#home">Back to top</a>
					
					<h3 id="CVI">Article VI</h3>
					<h4>Quorum</h4>
					<p>Section A. A minimum of 2 Executive Officers and 50% of the membership must be present to conduct official business of the organization. Signature lists shall be maintained of those attending each meeting.</p>
					<p>Section B. Once quorum is established, at least a majority vote and 50% +1 member present is needed to pass a regular motion placed on the table.</p>
					<a href="#home">Back to top</a>
				
					<h3 id="CVII">Article VII</h3>
					<h4>Elections</h4>
					<p>Section A. The President shall appoint a chair for the Election's Committee with the approval of the organization. Elections will be held at the last meeting of the academic year.</p>
					<p>Section B. Only regular members who are currently enrolled and in good standing are eligible to vote.</p>
					<p>Section C. A majority vote- 50% + 1 shall be necessary to elect officers.</p>
					<p>Section D. All officers shall hold office until new officers have been selected.</p>
					<p>Section E. The term of office shall be at least one year. No officer shall serve in the same position for more than two academic years.</p>
					<a href="#home">Back to top</a>
					
					<h3 id="CVIII">Article VIII</h3>
					<h4>Members in Good Standing</h4>
					<p>Members who are currently enrolled, are active in the organization, and attend at least 3 meetings each semester, shall be considered members in good standing. Only members in good standing have the right to vote on organization business.</p>
					<a href="#home">Back to top</a>
					
					<h3 id="CIX">Article IX</h3>
					<h4>Vacances</h4>
					<p>A vacancy in the position of President shall be filed by the Vice President who shall become the President.</p>
					<p>Vacancies in the positions of Vice President, Secretary, or Treasurer shall be filled by a special election following the normal election procedure.</p>
					<a href="#home">Back to top</a>
				
					<h3 id="CX">Article X</h3>
					<h4>Impeachment</h4>
					<ol>
						<li>Officers of the organization may be impeached for: 
							<ol>
								<li>failure to attend 50% of the regularly scheduled meetings without justifiable reasons</li>
								<li>failure to fulfill the duties of office as outlined in this Constitution.</li>
							</ol>
						</li>
						<li> Impeachment charges can be brought forward by any member. The charges will be presented at one meeting and delivered by mail to the individual being charged.</li>
						<li> A special meeting, at which there must be a quorum, will be held within 14 days of the initial presentation of the charges at which time the individual being charged will be able to answer the charges.</li>
						<li>A 2/3 majority vote of those present at the meeting is necessary for impeachment and removal from office.</li>
						<li>Vacancies created by impeachment shall be filled as prescribed in Article IX.</li>
					</ol>
					<a href="#home">Back to top</a>
				
					<h3 id="CXI">Article XI</h3>
					<h4>Amendments</h4>
					<p>Section A. This Constitution may be amended by a 2/3 majority vote of the membership at the first meeting of the organization at which a quorum is present following the approval of the amendment by the Executive Committee.</p>
					<p>Section B. Amendments must be presented to the Executive Committee for approval before they may be submitted to this organization membership of a vote.</p>
					<p>Section C. Amendments shall become effective following approval of the Student Government Charter Committee and the Office of Student Activities.</p>
